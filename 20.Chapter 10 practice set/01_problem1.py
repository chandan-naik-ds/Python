class Programmer():
    company = "Microsoft"
    
    def __init__(self,name,id,salary):
        self.name = name
        self.id = id
        self.salary = salary

m = Programmer("Chandan",102,1200000)
print(m.name,m.id,m.salary,m.company)
r = Programmer("Ravi",110,1200000)
print(r.name,r.id,m.salary,r.company)















